export function isLocale(value: string | null | undefined): value is Locale {
  return Boolean(value && locales.includes(value as Locale));
}

export function translate(key: TranslationKey | string, locale: Locale): string {
  const localized = translations[locale]?.[key as TranslationKey];
  if (localized) return localized;
  return en[key as TranslationKey] || key;
}

export function translateVisibleText(text: string, locale: Locale): string {
  const trimmed = text.trim();
  if (!trimmed) return text;
  const englishEntry = Object.entries(en).find(([, value]) => value === trimmed);
  const localizedEntry = englishEntry || Object.values(translations)
    .flatMap((map) => Object.entries(map))
    .find(([, value]) => value === trimmed);
  if (!localizedEntry) return text;
  const translated = translate(localizedEntry[0], locale);
  return text.replace(trimmed, translated);
}
